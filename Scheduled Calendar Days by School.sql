SELECT schools."NAME", sum(PS_CALENDAR_DAY.INSESSION)

FROM PS_CALENDAR_DAY
	JOIN SCHOOLS ON PS_CALENDAR_DAY.SCHOOLID = SCHOOLS.school_number

WHERE 
	PS_CALENDAR_DAY.DATE_VALUE BETWEEN to_date('09/01/2019','mm/dd/yyyy') AND to_date('03/13/2020','mm/dd/yyyy')
	AND PS_CALENDAR_DAY.SCHOOLID IN (1925,1923,3000,9404,1153,1157,2988,1705,798,2062,1938)

GROUP BY
	schools.name
	
ORDER BY
	schools."NAME"